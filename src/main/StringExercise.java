package main;

public class StringExercise {
    public static void main(String[] args) {
        String str1 = "We don't need no education";
        System.out.println(str1 + '\n');

        String str2 = "We don't need no thought control";
        System.out.println(str2 + '\n');

        String str3 = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(str3 + "\n");

        String str4 = "In windows, the main drive is usually C:\\";
        System.out.println(str4 + '\n');

        String str5 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(str5);
    }
}
